public class POJO
{
  public double a1;
  public double a2;
  public double a3;
  public double a4;

  public POJO()
  {
  }

  public POJO(double a1, double a2, double a3, double a4)
  {
    this.a1 = a1;
    this.a2 = a2;
    this.a3 = a3;
    this.a4 = a4;
  }

  public double getA1()
  {
    return a1;
  }

  public void setA1(double a1)
  {
    this.a1 = a1;
  }

  public double getA2()
  {
    return a2;
  }

  public void setA2(double a2)
  {
    this.a2 = a2;
  }

  public double getA3()
  {
    return a3;
  }

  public void setA3(double a3)
  {
    this.a3 = a3;
  }

  public double getA4()
  {
    return a4;
  }

  public void setA4(double a4)
  {
    this.a4 = a4;
  }
}
